                var reqAdapter = invocationContext.GetRequestAdapter();
                var requestInfo = ToGetRequestInformation(q => {
                    q.QueryParameters.UserId = userId;
                    if (!string.IsNullOrEmpty(title)) q.QueryParameters.Title = title;
